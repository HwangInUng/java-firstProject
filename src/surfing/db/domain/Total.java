package surfing.db.domain;

public class Total {
	private int total_idx;
	private String name;
	private int nomal;
	private int couple;
	private int single;
	private int board;
	private int food;
	private int etc;

	public int getTotal_idx() {
		return total_idx;
	}

	public void setTotal_idx(int total_idx) {
		this.total_idx = total_idx;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getNomal() {
		return nomal;
	}

	public void setNomal(int nomal) {
		this.nomal = nomal;
	}

	public int getCouple() {
		return couple;
	}

	public void setCouple(int couple) {
		this.couple = couple;
	}

	public int getSingle() {
		return single;
	}

	public void setSingle(int single) {
		this.single = single;
	}

	public int getBoard() {
		return board;
	}

	public void setBoard(int board) {
		this.board = board;
	}

	public int getFood() {
		return food;
	}

	public void setFood(int food) {
		this.food = food;
	}

	public int getEtc() {
		return etc;
	}

	public void setEtc(int etc) {
		this.etc = etc;
	}

}
